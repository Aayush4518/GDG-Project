def get_latest_location_by_tourist_id(db: Session, tourist_id: str) -> Optional[models.LocationLog]:
    """
    Get the latest location log entry for a specific tourist
    
    This function retrieves the most recent location log for the specified
    tourist, ordered by timestamp in descending order.
    
    Args:
        db: Database session for executing the query
        tourist_id: String representation of the tourist's UUID
        
    Returns:
        Optional[models.LocationLog]: Latest location log if found, None otherwise
    """
    return db.query(models.LocationLog).filter(
        models.LocationLog.tourist_id == tourist_id
    ).order_by(models.LocationLog.timestamp.desc()).first()


